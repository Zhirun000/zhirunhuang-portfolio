function bindCards() {
  document.querySelectorAll(".file-card").forEach((card) => {
    const project = projects.find((item) => item.index === card.dataset.index);

    card.addEventListener("mouseenter", () => {
      if (project.index === "UP") return;
      playChime();
    });
    card.addEventListener("mouseleave", hidePreview);
    card.addEventListener("blur", hidePreview);
    card.addEventListener("click", (event) => {
      if (project.index === "UP") return;
      openWindow(project, event.currentTarget);
    });
  });
}

function showPreview(project, event) {
  previewImage.style.setProperty("--preview-tone", "#89918e");
  previewImage.dataset.code = project.code;
  const previewSource = project.hoverImage || project.image;
  previewImage.classList.toggle("has-real-image", Boolean(previewSource));
  previewImage.style.backgroundImage = previewSource ? `url("${previewSource}")` : "";
  previewIndex.textContent = project.index;
  previewTitle.textContent = project.title;
  previewClient.textContent = `${project.client} / ${project.year}`;
  preview.classList.add("is-visible");
  movePreview(event);
}

function movePreview(event) {
  if (!event || !event.clientX) return;
  const width = Math.min(430, window.innerWidth * 0.34);
  const x = Math.min(event.clientX + 24, window.innerWidth - width - 24);
  const y = Math.min(event.clientY + 24, window.innerHeight - 360);
  preview.style.left = `${Math.max(16, x)}px`;
  preview.style.top = `${Math.max(56, y)}px`;
}

function hidePreview() {
  preview.classList.remove("is-visible");
}

function paintArtwork(element, project) {
  const artworkSource = project.contentImage || project.hoverImage || project.image;
  element.style.setProperty("--work-tone", "#89918e");
  element.dataset.code = project.code;
  element.innerHTML = "";
  element.classList.toggle("is-upcoming-preview", project.index === "UP");
  element.classList.toggle("has-gallery", Array.isArray(artworkSource));
  element.classList.toggle("has-real-image", Boolean(artworkSource) && !Array.isArray(artworkSource));

  if (project.index === "UP") {
    element.classList.remove("has-gallery");
    element.classList.add("has-real-image");
    element.removeAttribute("data-code");
    element.style.backgroundImage = "";
    element.innerHTML = `
      <div class="upcoming-art">
        ${artworkSource ? `<img src="${artworkSource}" alt="${project.title} preview">` : ""}
        <span>UP COMING</span>
      </div>
    `;
    return;
  }

  if (Array.isArray(artworkSource)) {
    element.style.backgroundImage = "";
    element.removeAttribute("data-code");
    element.innerHTML = artworkSource
      .map((item) => {
        if (typeof item === "string") {
          return `<img src="${item}" alt="${project.title} content">`;
        }

        if (item.type === "video") {
          return `
            <figure class="gallery-video">
              <video src="${item.src}" controls preload="metadata"></video>
              <figcaption>Project video - click play to view</figcaption>
            </figure>
          `;
        }

        return "";
      })
      .join("");
    return;
  }

  element.style.backgroundImage = artworkSource ? `url("${artworkSource}")` : "";
}

function positionWindowNearCard(card) {
  const tag = card.querySelector(".folder-tag");
  const rect = (tag || card).getBoundingClientRect();
  const width = Math.min(760, Math.max(360, window.innerWidth * 0.58));
  const height = Math.min(560, Math.max(280, window.innerHeight * 0.68));
  const x = Math.min(rect.right + 12, window.innerWidth - width - 12);
  const y = Math.min(rect.top - 18, window.innerHeight - height - 12);

  workWindow.style.width = `${width}px`;
  workWindow.style.height = `${height}px`;
  workWindow.style.left = `${Math.max(8, x)}px`;
  workWindow.style.top = `${Math.max(48, y)}px`;
  workWindow.style.transform = "none";
}

function openWindow(project, card) {
  activeProject = project;
  hidePreview();
  windowTitle.textContent = project.title;
  windowCount.textContent = `(${project.index}/${projects.length})`;
  paintArtwork(windowArt, project);
  positionWindowNearCard(card);
  workWindow.classList.toggle("is-upcoming-window", project.index === "UP");
  workWindow.classList.add("is-open");
  workWindow.setAttribute("aria-hidden", "false");
}

function closeWindow() {
  workWindow.classList.remove("is-open");
  workWindow.setAttribute("aria-hidden", "true");
}

