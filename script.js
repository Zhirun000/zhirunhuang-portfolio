windowBar.addEventListener("pointerdown", (event) => {
  if (event.target.closest("button")) return;
  const rect = workWindow.getBoundingClientRect();
  dragOffset = {
    x: event.clientX - rect.left,
    y: event.clientY - rect.top
  };
  workWindow.style.transform = "none";
  windowBar.setPointerCapture(event.pointerId);
});

windowBar.addEventListener("pointermove", (event) => {
  if (!dragOffset) return;
  const maxX = window.innerWidth - workWindow.offsetWidth - 8;
  const maxY = window.innerHeight - workWindow.offsetHeight - 8;
  const x = Math.max(8, Math.min(event.clientX - dragOffset.x, maxX));
  const y = Math.max(48, Math.min(event.clientY - dragOffset.y, maxY));
  workWindow.style.left = `${x}px`;
  workWindow.style.top = `${y}px`;
});

windowBar.addEventListener("pointerup", (event) => {
  dragOffset = null;
  if (windowBar.hasPointerCapture(event.pointerId)) {
    windowBar.releasePointerCapture(event.pointerId);
  }
});

