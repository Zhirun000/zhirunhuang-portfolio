function getAudioContext() {
  if (!audioContext) {
    audioContext = new (window.AudioContext || window.webkitAudioContext)();
  }
  if (audioContext.state === "suspended") {
    audioContext.resume();
  }
  return audioContext;
}

function playTone(context, frequency, start, duration, gainValue) {
  const oscillator = context.createOscillator();
  const gain = context.createGain();
  oscillator.type = "triangle";
  oscillator.frequency.setValueAtTime(frequency, start);
  oscillator.frequency.exponentialRampToValueAtTime(frequency * 1.012, start + duration);
  gain.gain.setValueAtTime(0, start);
  gain.gain.linearRampToValueAtTime(gainValue, start + 0.01);
  gain.gain.exponentialRampToValueAtTime(0.0001, start + duration);
  oscillator.connect(gain);
  gain.connect(context.destination);
  oscillator.start(start);
  oscillator.stop(start + duration + 0.02);
}

function playChime() {
  const now = performance.now();
  if (now - lastChimeAt < 90) return;
  lastChimeAt = now;

  const context = getAudioContext();
  const start = context.currentTime;
  playTone(context, 880, start, 0.09, 0.035);
  playTone(context, 1320, start + 0.045, 0.08, 0.026);
  playTone(context, 1760, start + 0.088, 0.075, 0.018);
}

